import '../../domain/entities/property.dart';

class PropertyModel extends Property {
  const PropertyModel({
    required super.id,
    required super.title,
    required super.description,
    required super.price,
    required super.type,
    required super.bedrooms,
    required super.bathrooms,
    required super.area,
    required super.images,
    required super.location,
    required super.latitude,
    required super.longitude,
    required super.brokerName,
    required super.brokerPhone,
    required super.brokerPhoto,
    required super.amenities,
    required super.isFurnished,
    required super.yearBuilt,
    required super.listedDate,
    required super.status,
  });

  factory PropertyModel.fromJson(Map<String, dynamic> json) {
    return PropertyModel(
      id: json['id'] as String,
      title: json['title'] as String,
      description: json['description'] as String,
      price: (json['price'] as num).toDouble(),
      type: json['type'] as String,
      bedrooms: json['bedrooms'] as int,
      bathrooms: json['bathrooms'] as int,
      area: (json['area'] as num).toDouble(),
      images: List<String>.from(json['images'] as List),
      location: json['location'] as String,
      latitude: (json['latitude'] as num).toDouble(),
      longitude: (json['longitude'] as num).toDouble(),
      brokerName: json['brokerName'] as String,
      brokerPhone: json['brokerPhone'] as String,
      brokerPhoto: json['brokerPhoto'] as String,
      amenities: List<String>.from(json['amenities'] as List),
      isFurnished: json['isFurnished'] as bool,
      yearBuilt: json['yearBuilt'] as int,
      listedDate: json['listedDate'] as String,
      status: json['status'] as String,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'title': title,
      'description': description,
      'price': price,
      'type': type,
      'bedrooms': bedrooms,
      'bathrooms': bathrooms,
      'area': area,
      'images': images,
      'location': location,
      'latitude': latitude,
      'longitude': longitude,
      'brokerName': brokerName,
      'brokerPhone': brokerPhone,
      'brokerPhoto': brokerPhoto,
      'amenities': amenities,
      'isFurnished': isFurnished,
      'yearBuilt': yearBuilt,
      'listedDate': listedDate,
      'status': status,
    };
  }

  Property toEntity() => Property(
    id: id,
    title: title,
    description: description,
    price: price,
    type: type,
    bedrooms: bedrooms,
    bathrooms: bathrooms,
    area: area,
    images: images,
    location: location,
    latitude: latitude,
    longitude: longitude,
    brokerName: brokerName,
    brokerPhone: brokerPhone,
    brokerPhoto: brokerPhoto,
    amenities: amenities,
    isFurnished: isFurnished,
    yearBuilt: yearBuilt,
    listedDate: listedDate,
    status: status,
  );

  factory PropertyModel.fromEntity(Property entity) => PropertyModel(
    id: entity.id,
    title: entity.title,
    description: entity.description,
    price: entity.price,
    type: entity.type,
    bedrooms: entity.bedrooms,
    bathrooms: entity.bathrooms,
    area: entity.area,
    images: entity.images,
    location: entity.location,
    latitude: entity.latitude,
    longitude: entity.longitude,
    brokerName: entity.brokerName,
    brokerPhone: entity.brokerPhone,
    brokerPhoto: entity.brokerPhoto,
    amenities: entity.amenities,
    isFurnished: entity.isFurnished,
    yearBuilt: entity.yearBuilt,
    listedDate: entity.listedDate,
    status: entity.status,
  );
}
